    /**
     * Returns a full Kenyan name where the first name and last name
     * always belong to the same gender — matching real Kenyan naming patterns
     * where gender, religion, and tribe play hand in hand.
     *
     * Examples:
     *   male   → "John Mwangi", "Nelson Kiptoo", "Hassan Odhiambo"
     *   female → "Damaris Wangari", "Grace Chepkemoi", "Amina Adhiambo"
     *
     * @param string|null $gender 'male', 'female', or null for random
     * @return string
     */
    public function name($gender = null)
    {
        if ($gender === null) {
            $gender = static::randomElement(['male', 'female']);
        }

        if (strtolower($gender) === 'female') {
            return static::randomElement(static::$firstNameFemale)
                . ' '
                . static::randomElement(static::$lastNameFemale);
        }

        return static::randomElement(static::$firstNameMale)
            . ' '
            . static::randomElement(static::$lastNameMale);
    }

